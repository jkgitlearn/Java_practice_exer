package mixedPractice;

public class Even_Odd_If {

	public static void main(String[] args) { /* check if given number is odd. 
	                                            if even print:the number is even or 
	                                            if odd print:the number is odd*/ 
		int e =232;
		
		if (e%2==0){
			System.out.println(+e + " is an even number");
		}else {
			System.out.println(+e + " is an odd number");
		}
	}
}


